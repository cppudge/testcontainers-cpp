MosquittoImage::MosquittoImage()
    : image_(GenericImage::from_reference(std::string(kDefaultImage))) {
    // Baked once, never touched by rendering. The broker prints "mosquitto
    // version X.Y.Z running" AFTER its listening sockets are open (verified
    // in the 2.0.x sources: the line follows listeners__start()), so there is
    // no log/listener race to force a TCP or exec probe over — and an exec
    // probe would break under user-replaced auth configs. Default log_dest is
    // stderr; the either-stream wait survives a replacement config's
    // `log_dest stdout`.
    image_.with_exposed_port(tcp(kPort)).with_wait(wait_for::log("running"));
}

