GenericImage MosquittoImage::to_generic() const {
    // Fail fast, before any daemon contact: mosquitto's config is order-
    // sensitive and listener-scoped, so "merging" option lines into a
    // user-owned file has no well-defined meaning.
    if (user_config_.has_value() && !config_options_.empty()) {
        throw Error("with_config_option cannot be combined with a replacement config "
                    "(with_config / with_config_content) — mosquitto options are order- and "
                    "listener-scoped, so put the line in your config file instead");
    }

    // Render into a COPY: repeated to_generic()/start() calls must never
    // accumulate state in the config.
    GenericImage generic = image_;
    if (user_config_.has_value()) {
        generic.with_copy_to(*user_config_);
    } else {
        // The managed config: what the image's own no-auth quickstart file
        // ships, generated as a pure function of the options — no timestamps —
        // so identical configs stay reuse-hash-identical (byte sources
        // contribute content verbatim to the hash).
        std::string conf = "# Generated by testcontainers-cpp (MosquittoImage).\n"
                           "listener " +
                           std::to_string(kPort) +
                           "\n"
                           "allow_anonymous true\n";
        for (const auto& [key, value] : config_options_) {
            conf += key + " " + value + "\n";
        }
        generic.with_copy_to(CopyToContainer::content(std::move(conf), std::string(kConfigPath)));
    }

    // Customizers last: what the escape hatch sets wins over the module's
    // rendering, mirroring how with_create_body_patch wins over typed fields.
    for (const auto& customize : customizers_) {
        customize(generic);
    }
    return generic;
}

MosquittoContainer MosquittoImage::start() const {
    return MosquittoContainer(to_generic().start());
}

